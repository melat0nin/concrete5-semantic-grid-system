<?php
require 'jsmin.php';

if ( filemtime('plugins.js') > filemtime('application.min.js') ||
     filemtime('application.js') > filemtime('application.min.js') ) {

    $plugin_output = JSMin::minify(file_get_contents('plugins.js'));
    $application_output = JSMin::minify(file_get_contents('application.js'));
    $output = $plugin_output . $application_output;
    file_put_contents('application.min.js', $output);
  
}

header("Content-type: application/javascript");
echo file_get_contents('application.min.js');
?>